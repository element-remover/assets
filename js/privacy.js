  markup = (obj, t = "div", c = "", only = false) => {
    let keys = Object.keys(obj),
      tag = t,
      cl = c,
      content = "";
    keys.map(k => {
      let v = obj[k];
      k = (a = k.split("."), a.length == 2 ? (cl = a[1], a[0]) : a[0]);

      if (/p/.test(k)) {
        content += cr(k, "", v)
      } else if (k == "arr") {
        content += v.map(l => cr("li", "", l)).join("")
      } else if (k == "ul") {
        content += cr(k, cl, markup(v, k, cl, 1))
      } else if (typeof v == "string") {
        content += cr(tag, k, v)
      } else if (k == 'i') {
        content += cr("div", k, v.map(m => markup(m, "div", "", m['bi'] || m['fu'] ? 0 : 1)).join(""))
      } else if (typeof v == "object") {
        cl = k
        content += markup(v, "div", k, 1)
      }
    })
    return only ? content : cr(tag, cl, content);
  },
